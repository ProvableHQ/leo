    /// Returns the number of arguments required by the instruction.
    pub fn num_args(&self) -> usize {
        match self {
            Self::ChaChaRand(_) => 0,
            Self::GroupGen => 0,
            Self::SelfAddress => 0,
            Self::SelfCaller => 0,
            Self::SelfChecksum => 0,
            Self::SelfEdition => 0,
            Self::SelfId => 0,
            Self::SelfProgramOwner => 0,
            Self::SelfSigner => 0,
            Self::BlockHeight => 0,
            Self::BlockTimestamp => 0,
            Self::NetworkId => 0,

            Self::Commit(_, _) => 2,
            Self::Hash(_, _) => 1,
            Self::ECDSAVerify(_) => 3,

            Self::MappingGet => 2,
            Self::MappingSet => 3,
            Self::MappingGetOrUse => 3,
            Self::MappingRemove => 2,
            Self::MappingContains => 2,

            Self::OptionalUnwrap => 1,
            Self::OptionalUnwrapOr => 2,

            Self::VectorGet => 2,
            Self::VectorSet => 3,
            Self::VectorPush => 2,
            Self::VectorLen => 1,
            Self::VectorClear => 1,
            Self::VectorPop => 1,
            Self::VectorSwapRemove => 2,

            Self::GroupToXCoordinate => 1,
            Self::GroupToYCoordinate => 1,

            Self::SignatureVerify => 3,
            Self::FutureAwait => 1,

            Self::ProgramChecksum => 1,
            Self::ProgramEdition => 1,
            Self::ProgramOwner => 1,

            Self::Serialize(_) => 1,
            Self::Deserialize(_, _) => 1,

            Self::CheatCodePrintMapping => 1,
            Self::CheatCodeSetBlockHeight => 1,
            Self::CheatCodeSetBlockTimestamp => 1,
            Self::CheatCodeSetSigner => 1,
        }
    }

    /// Returns whether or not this function is finalize command.
    pub fn is_finalize_command(&self) -> bool {
        match self {
            Intrinsic::FutureAwait
            | Intrinsic::ChaChaRand(_)
            | Intrinsic::ECDSAVerify(_)
            | Intrinsic::MappingGet
            | Intrinsic::MappingSet
            | Intrinsic::MappingGetOrUse
            | Intrinsic::VectorGet
            | Intrinsic::VectorSet
            | Intrinsic::MappingRemove
            | Intrinsic::MappingContains
            | Intrinsic::ProgramChecksum
            | Intrinsic::ProgramEdition
            | Intrinsic::ProgramOwner
            | Intrinsic::VectorPush
            | Intrinsic::VectorLen
            | Intrinsic::VectorClear
            | Intrinsic::VectorPop
            | Intrinsic::VectorSwapRemove => true,
            Intrinsic::Commit(_, _)
            | Intrinsic::Hash(_, _)
            | Intrinsic::OptionalUnwrap
            | Intrinsic::OptionalUnwrapOr
            | Intrinsic::GroupToXCoordinate
            | Intrinsic::GroupToYCoordinate
            | Intrinsic::GroupGen
            | Intrinsic::SelfAddress
            | Intrinsic::SelfCaller
            | Intrinsic::SelfChecksum
            | Intrinsic::SelfEdition
            | Intrinsic::SelfId
            | Intrinsic::SelfProgramOwner
            | Intrinsic::SelfSigner
            | Intrinsic::BlockHeight
            | Intrinsic::BlockTimestamp
            | Intrinsic::NetworkId
            | Intrinsic::SignatureVerify
            | Intrinsic::Serialize(_)
            | Intrinsic::Deserialize(_, _)
            | Intrinsic::CheatCodePrintMapping
            | Intrinsic::CheatCodeSetBlockHeight
            | Intrinsic::CheatCodeSetBlockTimestamp
            | Intrinsic::CheatCodeSetSigner => false,
        }
    }

    /// Returns whether or not this function has side effects
    pub fn is_pure(&self) -> bool {
        match self {
            Intrinsic::FutureAwait
            | Intrinsic::VectorSet
            | Intrinsic::VectorPush
            | Intrinsic::VectorClear
            | Intrinsic::CheatCodePrintMapping
            | Intrinsic::CheatCodeSetBlockHeight
            | Intrinsic::CheatCodeSetBlockTimestamp
            | Intrinsic::CheatCodeSetSigner
            | Intrinsic::VectorPop
            // Pedersen64 and Pedersen128 operations can fail for large inputs.
            | Intrinsic::Hash(
                HashVariant::HashPED64
                | HashVariant::HashPED64Raw
                | HashVariant::HashPED128
                | HashVariant::HashPED128Raw,
                _,
            )
            // Mapping operations have side effects
            | Intrinsic::MappingRemove
            | Intrinsic::MappingGet
            | Intrinsic::MappingSet
            | Intrinsic::MappingGetOrUse
            | Intrinsic::MappingContains
            | Intrinsic::VectorSwapRemove => false,

            Intrinsic::ChaChaRand(_)
            | Intrinsic::ECDSAVerify(_)
            | Intrinsic::ProgramChecksum
            | Intrinsic::ProgramEdition
            | Intrinsic::ProgramOwner
            | Intrinsic::VectorLen
            | Intrinsic::VectorGet
            | Intrinsic::Commit(_, _)
            | Intrinsic::Hash(_, _)
            | Intrinsic::OptionalUnwrap
            | Intrinsic::OptionalUnwrapOr
            | Intrinsic::GroupToXCoordinate
            | Intrinsic::GroupToYCoordinate
            | Intrinsic::GroupGen
            | Intrinsic::SelfAddress
            | Intrinsic::SelfCaller
            | Intrinsic::SelfChecksum
            | Intrinsic::SelfEdition
            | Intrinsic::SelfId
            | Intrinsic::SelfProgramOwner
            | Intrinsic::SelfSigner
            | Intrinsic::BlockHeight
            | Intrinsic::BlockTimestamp
            | Intrinsic::NetworkId
            | Intrinsic::SignatureVerify
            | Intrinsic::Serialize(_)
            | Intrinsic::Deserialize(_, _) => true,
        }
    }
